package vanoni.ChordalGraphs;

public class Vertex {

	private int alpha;
	private int size;

	/**
	 * @return the position
	 */
	public int getAlpha() {
		return alpha;
	}

	/**
	 * @param position the position to set
	 */
	public void setPosition(int alpha) {
		this.alpha = alpha;
	}

	/**
	 * @return the size
	 */
	public int getSize() {
		return size;
	}

	/**
	 * @param size the size to set
	 */
	public void setSize(int size) {
		this.size = size;
	}
	
}
